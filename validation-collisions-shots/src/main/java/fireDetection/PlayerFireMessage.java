package fireDetection;

public class PlayerFireMessage {

    private String id;

    private int[] direction;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int[] getDirection() {
        return direction;
    }

    public void setDirection(int[] direction) {
        this.direction = direction;
    }
}
